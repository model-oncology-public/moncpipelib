class TestSCD2ReconcileSerialization:
    """Verify the per-target advisory lock serializes concurrent reconciles
    against the same target without over-serializing across targets (#278).

    Uses state-based assertions (``pg_stat_activity`` polling for
    ``wait_event = 'advisory'``) rather than wall-clock timing, so the
    test is robust under CI load.
    """

    @pytest.fixture(autouse=True)
    def setup(
        self,
        scd2_table_builder: SCD2TableBuilder,
        pg_connection: psycopg.Connection,
        postgres_resource,
        pg_connection_params: dict[str, Any],
    ):
        self.table_a = scd2_table_builder.create_table(
            table_name=f"dim_lock_a_{uuid.uuid4().hex[:8]}",
            business_key_columns={"product_id": "TEXT"},
            tracked_columns={"product_name": "TEXT"},
            include_unique_index=False,
        )
        self.table_b = scd2_table_builder.create_table(
            table_name=f"dim_lock_b_{uuid.uuid4().hex[:8]}",
            business_key_columns={"product_id": "TEXT"},
            tracked_columns={"product_name": "TEXT"},
            include_unique_index=False,
        )
        self.conn = pg_connection
        self.resource = postgres_resource
        self.params = pg_connection_params
        self.builder = scd2_table_builder
        yield
        self.builder.drop(self.table_a)
        self.builder.drop(self.table_b)

    def _run_reconcile_in_thread(
        self,
        target: str,
        *,
        result_holder: list[dict[str, int]],
        error_holder: list[BaseException],
    ):
        def _run() -> None:
            try:
                result_holder.append(
                    self.resource.reconcile_scd2(
                        target=target,
                        business_key=["product_id"],
                        scd2=SCD2Config(sequence_col=None),
                        run_id="test-run",
                    )
                )
            except BaseException as exc:  # noqa: BLE001
                error_holder.append(exc)

        import threading

        t = threading.Thread(target=_run, name=f"reconcile-{target}")
        t.start()
        return t

    def _wait_for_advisory_wait(self, lock_holder_pid: int, timeout: float) -> bool:
        """Poll pg_stat_activity for any backend (other than ``lock_holder_pid``)
        waiting on an advisory lock. Returns True if observed within timeout.
        """
        import time

        deadline = time.time() + timeout
        while time.time() < deadline:
            with self.conn.cursor() as cur:
                # Roll back the autocommit-off connection's implicit txn so
                # snapshots of pg_stat_activity stay fresh between polls.
                self.conn.rollback()
                cur.execute(
                    "SELECT pid, wait_event_type, wait_event "
                    "FROM pg_stat_activity "
                    "WHERE wait_event = 'advisory' AND pid <> %s",
                    (lock_holder_pid,),
                )
                if cur.fetchone() is not None:
                    return True
            time.sleep(0.1)
        return False

    def test_same_target_concurrent_reconciles_serialize(self):
        """Two reconciles against the same target serialize via advisory lock.

        State-based assertion: while conn A holds the lock, thread B's
        reconcile is observed in pg_stat_activity with
        ``wait_event = 'advisory'``. After A commits, B completes.
        """
        target = self.table_a

        # Conn A: acquire the advisory lock manually, do not commit.
        conn_a = psycopg.connect(**self.params)
        conn_a.autocommit = False
        try:
            with conn_a.cursor() as cur:
                cur.execute(
                    "SELECT pg_advisory_xact_lock(hashtext(%s))",
                    (target,),
                )
                cur.execute("SELECT pg_backend_pid()")
                row = cur.fetchone()
                assert row is not None
                pid_a = int(row[0])

            # Thread B: reconcile_scd2 against the same target.
            results: list[dict[str, int]] = []
            errors: list[BaseException] = []
            thread_b = self._run_reconcile_in_thread(
                target, result_holder=results, error_holder=errors
            )

            # State assertion: thread B should be observed waiting on the
            # advisory lock. Generous timeout for CI load.
            observed = self._wait_for_advisory_wait(pid_a, timeout=15.0)
            assert observed, (
                "Thread B did not appear in pg_stat_activity with "
                "wait_event='advisory' within 15s -- the per-target lock "
                "did not serialize the second reconcile."
            )

            # Release A's lock by committing.
            conn_a.commit()

            # Thread B should now complete promptly.
            thread_b.join(timeout=30.0)
            assert not thread_b.is_alive(), (
                "Thread B did not complete after lock released within 30s."
            )
            assert not errors, f"Thread B raised: {errors[0]!r}"
            assert results, "Thread B returned no result"
        finally:
            try:
                conn_a.rollback()
            finally:
                conn_a.close()

    def test_different_target_concurrent_reconciles_do_not_block(self):
        """Holding the lock for target_a does not block reconcile of target_b.

        Guards against accidental over-serialization (e.g. if someone changes
        the lock key to a constant).
        """
        conn_a = psycopg.connect(**self.params)
        conn_a.autocommit = False
        try:
            with conn_a.cursor() as cur:
                cur.execute(
                    "SELECT pg_advisory_xact_lock(hashtext(%s))",
                    (self.table_a,),
                )

            # Thread B: reconcile target_b. Should not block on target_a's lock.
            results: list[dict[str, int]] = []
            errors: list[BaseException] = []
            thread_b = self._run_reconcile_in_thread(
                self.table_b, result_holder=results, error_holder=errors
            )

            # Loose upper bound: against a different-key lock the call should
            # not block at all. A 30s ceiling tolerates fixture/teardown noise
            # but would fail decisively if the lock were keyed on a constant.
            thread_b.join(timeout=30.0)
            assert not thread_b.is_alive(), (
                "Thread B (different-target reconcile) did not complete "
                "within 30s while the lock for target_a was held -- "
                "advisory lock is over-serializing across distinct targets."
            )
            assert not errors, f"Thread B raised: {errors[0]!r}"
            assert results, "Thread B returned no result"
        finally:
            try:
                conn_a.rollback()
            finally:
                conn_a.close()


