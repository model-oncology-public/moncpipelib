class TestSCD2SelfModificationFix:
    """Verify the materialize-then-insert restructure of ``scd2_finalize``.

    Before #274, finalize issued a single self-referencing
    ``INSERT INTO target SELECT ... FROM staging LEFT JOIN target`` which
    became O(N^2) on empty targets when the planner picked Nested Loop
    Anti Join. The fix splits this into a CTAS Stage 1 (materializes the
    diff into a temp table) followed by a Stage 2 INSERT-from-diff with
    no JOIN against the target.

    These tests guard the structural property that Stage 2 has no JOIN
    node in its plan -- which is what makes the self-modification cycle
    impossible regardless of planner choice.
    """

    @pytest.fixture(autouse=True)
    def setup(
        self,
        scd2_table_builder: SCD2TableBuilder,
        scd2_verifier: SCD2Verifier,
        pg_connection: psycopg.Connection,
        io_manager_factory,
    ):
        # Mirror the production rxnorm_attributes shape: business key
        # plus a load_period partition column. include_unique_index=False
        # because the unique index would need to span (business_key,
        # load_period) for partition-scoped writes; not relevant here.
        self.table = scd2_table_builder.create_table(
            table_name=f"dim_product_selfmod_{uuid.uuid4().hex[:8]}",
            business_key_columns={"product_id": "TEXT"},
            tracked_columns={"product_name": "TEXT", "price": "NUMERIC"},
            extra_columns={"load_period": "DATE"},
            include_unique_index=False,
        )
        self.conn = pg_connection
        self.verifier = scd2_verifier
        self.io_mgr = io_manager_factory()
        self.builder = scd2_table_builder
        yield
        self.builder.drop(self.table)

    def _make_partitioned_df(self, n_rows: int, load_period: str) -> pl.DataFrame:
        """Build a staging frame of ``n_rows`` distinct business keys for
        ``load_period``. Hash includes price so re-loads with different
        prices are detected as changes."""
        df = pl.DataFrame(
            {
                "product_id": [f"PROD-{i:05d}" for i in range(n_rows)],
                "product_name": [f"Widget {i}" for i in range(n_rows)],
                "price": [float(i) + 0.99 for i in range(n_rows)],
                "load_period": [datetime.date.fromisoformat(load_period)] * n_rows,
            }
        )
        return _hash(df, columns=["product_name", "price", "load_period"])

    def test_partition_scoped_empty_target_correctness(self):
        """Stage 1 + Stage 2 against an empty target produce N current rows.

        This is the correctness regression guard against any SQL bug
        introduced by the CTAS + INSERT-from-diff restructure. The
        partition_column='load_period' shape is the exact predicate that
        triggered the original O(N^2) pathology in production.
        """
        df = self._make_partitioned_df(100, "2024-01-02")
