def _hash(df: pl.DataFrame, columns: list[str] | None = None) -> pl.DataFrame:
    """Add row_hash using the library's compute_row_hash."""
    cols = columns or [c for c in df.columns if c != "product_id"]
    return df.with_columns(compute_row_hash(cols))


def _execute_scd2(
    io_mgr: PostgresIOManager,
    conn: psycopg.Connection,
    fqn: str,
    df: pl.DataFrame,
    business_key: list[str],
    *,
    effective_from_col: str = "effective_from",
    effective_to_col: str = "effective_to",
    is_current_col: str = "is_current",
    hash_col: str = "row_hash",
    detect_deletes: bool = False,
    sequence_col: str | None = None,
    partition_column: str | None = None,
    partition_values: list[Any] | None = None,
    change_detection_work_mem: str | None = None,
    change_detection_statement_timeout: str | None = None,
) -> dict[str, int | str]:
    """Helper to call execute_scd2 with a real connection and commit."""
    context = make_mock_output_context()
    scd2 = SCD2Config(
        effective_from_col=effective_from_col,
        effective_to_col=effective_to_col,
        is_current_col=is_current_col,
        hash_col=hash_col,
        sequence_col=sequence_col,
    )
    # Phase D widened ``execute_scd2`` to accept ``WriterConfig`` first
    # (chunked-COPY in the staging insert).  Build it from the IO
    # manager's resource so the chunk size + bulk method match what
    # production would use.
    config = io_mgr.postgres_resource._get_writer_config()  # noqa: SLF001
    with conn.cursor() as cursor:
        stats = execute_scd2(
            config,
            cursor,
            fqn,
            df,
            business_key,
            scd2,
            context,
            detect_deletes=detect_deletes,
            partition_column=partition_column,
            partition_values=partition_values,
            change_detection_work_mem=change_detection_work_mem,
            change_detection_statement_timeout=change_detection_statement_timeout,
        )
        conn.commit()
    return stats


