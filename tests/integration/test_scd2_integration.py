def _insert_raw_scd2_row(
    conn: psycopg.Connection,
    fqn: str,
    *,
    business_key: dict[str, Any],
    effective_from: datetime.datetime,
    row_hash: str,
    extra: dict[str, Any] | None = None,
) -> None:
    """Insert a raw SCD2 row directly, bypassing the writer.

    Used by collapse-equivalence and plan-shape tests where we need precise
    control over the (bk, effective_from, hash) tuples that the reconcile
    sees -- the writer would prevent us from creating consecutive duplicates
    in a single partition.
    """
    cols = list(business_key) + ["effective_from", "row_hash"]
    vals: list[Any] = [*business_key.values(), effective_from, row_hash]
    if extra:
        cols.extend(extra)
        vals.extend(extra.values())
    placeholders = ", ".join(["%s"] * len(vals))
    col_list = ", ".join(f'"{c}"' for c in cols)
    with conn.cursor() as cur:
        cur.execute(f"INSERT INTO {fqn} ({col_list}) VALUES ({placeholders})", vals)
    conn.commit()


class TestSCD2ReconcileCollapsePlanShape:
    """Verify the collapse DELETE plans without a SubPlan (#277).

    Load-bearing structural assertion: PostgreSQL cannot transform
    ``NOT IN (subquery)`` into anti-join semantics through a CTE projection
    chain, producing a ``Filter: NOT (ANY (SubPlan))`` plan that is O(N x M)
    at scale. The ``ROW_NUMBER`` + ``USING ranked WHERE rn > 1`` rewrite
    plans cleanly as a hash/merge/nested-loop join. If anyone "simplifies"
    back to ``NOT IN``, this test fails because the plan tree contains a
    ``SubPlan`` node.
    """

    @pytest.fixture(autouse=True)
    def setup(
        self,
        scd2_table_builder: SCD2TableBuilder,
        pg_connection: psycopg.Connection,
        postgres_resource,
    ):
        # No unique-current index: collapse needs the consecutive-duplicate
        # rows to coexist as is_current=true (each partition sees only its
        # own rows in production), and the unique index would prevent that.
        self.table = scd2_table_builder.create_table(
            table_name=f"dim_collapse_plan_{uuid.uuid4().hex[:8]}",
            business_key_columns={"product_id": "TEXT"},
            tracked_columns={"product_name": "TEXT"},
            include_unique_index=False,
        )
        self.conn = pg_connection
        self.resource = postgres_resource
        self.builder = scd2_table_builder
        yield
        self.builder.drop(self.table)

    def _populate_with_consecutive_duplicates(self, n_keys: int = 200) -> None:
        """Insert ``n_keys`` business keys, each with 3 versions where v1
        and v2 share a hash (consecutive duplicate) and v3 is distinct."""
        base = datetime.datetime(2024, 1, 1, tzinfo=datetime.UTC)
        rows: list[tuple[Any, ...]] = []
        for i in range(n_keys):
            bk = f"PROD-{i:05d}"
            h1 = f"hash-{i % 50}-A"  # v1 and v2 same
            h2 = h1
            h3 = f"hash-{i % 50}-B"  # v3 distinct
            rows.append((bk, "Widget v1", base + datetime.timedelta(days=i, hours=0), h1))
            rows.append((bk, "Widget v2", base + datetime.timedelta(days=i, hours=1), h2))
