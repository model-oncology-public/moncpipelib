_END_OF_TIME = datetime.date.fromisoformat(SCD2Config().end_of_time)


def _is_end_of_time(val: object) -> bool:
    """Check if a value is the SCD2 end-of-time sentinel (date or datetime)."""
    if val is None:
        return False
    if isinstance(val, datetime.datetime):
        return val.date() == _END_OF_TIME
    if isinstance(val, datetime.date):
        return val == _END_OF_TIME
    return False


